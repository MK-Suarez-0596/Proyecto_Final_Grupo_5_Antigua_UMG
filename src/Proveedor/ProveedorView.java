    private void cleanCampos() {
        txtNit.setText("");
        txtProveedor.setText("");
        txtTelEmp.setText("");
        txtContacto.setText("");
        txtTelCont.setText("");
        txtDireccion.setText("");
    }

    private void cargarProveedor() {
        modelo.setRowCount(0);
        List<ProveedorModel> lista = ProveedorController.obtenerTodos();
        for (ProveedorModel p : lista) {
            modelo.addRow(new Object[] {
                    p.getNit(),
                    p.getProveedor(),
                    p.getTelef_empre(),
                    p.getContacto(),
                    p.getContacto(),
                    p.getDireccion()
            });
        }
    }

