    private void btn_addMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_btn_addMouseClicked
        ProveedorModel e = new ProveedorModel();

        e.setNit(txtNit.getText());
        e.setProveedor(txtProveedor.getText());
        e.setTelef_empre(txtTelEmp.getText());
        e.setContacto(txtContacto.getText());
        e.setTelef_contacto(txtTelCont.getText());
        e.setDireccion(txtDireccion.getText());

        if (ProveedorController.crear(e)) {
            JOptionPane.showMessageDialog(this, "Proveedor creado");
        }
        cargarProveedor();
    }

    private void btn_updateMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_btn_updateMouseClicked
        int fila = tblProveedor.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un Proveedor de la tabla para actualizar", "Advertencia",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        ProveedorModel e = new ProveedorModel();

        // Suponiendo que la primera columna del JTable contiene el ID
        String nit = tblProveedor.getValueAt(fila, 0).toString();
        e.setNit(nit);

        e.setProveedor(txtProveedor.getText());
        e.setTelef_empre(txtTelEmp.getText());
        e.setContacto(txtContacto.getText());
        e.setTelef_contacto(txtTelCont.getText());
        e.setDireccion(txtContacto.getText());

        if (ProveedorController.actualizar(e)) {
            JOptionPane.showMessageDialog(this, "Proveedor actualizado");
            cargarProveedor();
        }
    }// GEN-LAST:event_btn_updateMouseClicked

    private void btn_searchMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_btn_searchMouseClicked
        if (txtNit.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un NIT");
            return;
        }
        String nit = txtNit.getText();
        ProveedorModel p = ProveedorController.buscar(nit);
        modelo.setRowCount(0);
        if (p != null) {
            modelo.addRow(new Object[] {
                    p.getNit(),
                    p.getProveedor(),
                    p.getTelef_empre(),
                    p.getContacto(),
                    p.getTelef_contacto(),
                    p.getDireccion()
            });
        } else {
            JOptionPane.showMessageDialog(this, "Proveedor no encontrado");
        }
    }

    private void btn_deleteMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_btn_deleteMouseClicked
        int fila = tblProveedor.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un Proveedor");
            return;
        }

        String nit = tblProveedor.getValueAt(fila, 0).toString();
        int confirmar = JOptionPane.showConfirmDialog(this, "Â¿Eliminar NIT: " + nit + "?", "Confirmar",
                JOptionPane.YES_NO_OPTION);

        if (confirmar == JOptionPane.YES_OPTION) {
            if (ProveedorController.eliminar(nit)) {
                JOptionPane.showMessageDialog(this, "Proveedor eliminado");
                cargarProveedor();
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar");
            }
        }
    }// GEN-LAST:event_btn_deleteMouseClicked

    private void btn_clearMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_btn_clearMouseClicked
        cargarProveedor();
        cleanCampos();
    }// GEN-LAST:event_btn_clearMouseClicked

    private void btn_showallMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_btn_showallMouseClicked
        modelo = (DefaultTableModel) tblProveedor.getModel();
        modelo.setRowCount(0); // Limpia tabla

        for (ProveedorModel p : ProveedorController.obtenerTodos()) {
            Object[] fila = {
                    p.getNit(),
                    p.getProveedor(),
                    p.getTelef_empre(),
                    p.getContacto(),
                    p.getTelef_contacto(),
                    p.getDireccion()

            };
            modelo.addRow(fila);
        }
    }// GEN-LAST:event_btn_showallMouseClicked

